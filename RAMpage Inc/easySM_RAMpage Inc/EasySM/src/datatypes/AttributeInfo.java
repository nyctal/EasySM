package datatypes;

import store.*;
import store.Class;

public class AttributeInfo extends CDElementInfo {

	private store.Class ofClass;
	private Type type;

        public AttributeInfo(Class c, Type t, String n){
            setOfClass(c);
            setType(t);
            setName(n);
        }

        public void setOfClass(store.Class c){
            ofClass=c;
        }

        public void setType(Type t){
            type=t;
        }

        public store.Class getOfClass(){
            return ofClass;
        }

        public Type getType(){
            return type;
        }
}