package datatypes;

import java.util.*;

import store.*;


public class StateMachineInfo extends ElementInfo {

	private List<State> states;
	private List<Transition> trans;

        public StateMachineInfo (List<State> s, List<Transition> t){
            setStates(s);
            setTrans(t);
        }

        public void setStates(List<State> s){
            states=s;
        }

        public void setTrans(List<Transition> t){
            trans=t;
        }

        public List<State> getStates(){
            return states;
        }

        public List<Transition> getTrans(){
            return trans;
        }

}