package datatypes;
import store.Class;
/**
 *
 * @author davide
 */
public class AssociationInfo extends CDElementInfo{

    Class source;
    Class dest;
    String sourceName;
    String destName;

    public AssociationInfo(String n, Class s, Class d, String sn, String dn){
        setName(n);
        setSource(s);
        setDest(d);
        setSourceName(sn);
        setDestName(dn);
    }

    public void setDest(Class dest) {
        this.dest = dest;
    }

    public void setDestName(String destName) {
        this.destName = destName;
    }

    public void setSource(Class source) {
        this.source = source;
    }

    public void setSourceName(String sourceName) {
        this.sourceName = sourceName;
    }

    public Class getDest() {
        return dest;
    }

    public String getDestName() {
        return destName;
    }

    public Class getSource() {
        return source;
    }

    public String getSourceName() {
        return sourceName;
    }
}
